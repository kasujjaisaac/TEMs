<?php

/*
|--------------------------------------------------------------------------
| GET TENANT ID
|--------------------------------------------------------------------------
*/

$raw_tenant =
$_GET['tenant_id']
?? $_POST['tenant_id']
?? null;

/*
|--------------------------------------------------------------------------
| CLEAN TENANT ID
|--------------------------------------------------------------------------
*/

$safe_tenant_id = preg_replace(
    "/[^0-9]/",
    "",
    $raw_tenant ?: ''
);

/*
|--------------------------------------------------------------------------
| SESSION NAME
|--------------------------------------------------------------------------
*/

if ($safe_tenant_id) {

    session_name("Onyx_" . $safe_tenant_id);

} else {

    session_name("Onyx_default");
}

/*
|--------------------------------------------------------------------------
| SESSION COOKIE SETTINGS
|--------------------------------------------------------------------------
*/

session_set_cookie_params([

    'lifetime' => 86400,

    'path' => '/',

    'domain' => '',

    'secure' => isset($_SERVER['HTTPS']),

    'httponly' => true,

    'samesite' => 'Lax'
]);

/*
|--------------------------------------------------------------------------
| START SESSION
|--------------------------------------------------------------------------
*/

if (session_status() === PHP_SESSION_NONE) {

    session_start();
}

/*
|--------------------------------------------------------------------------
| GLOBAL LOGIN GUARD
|--------------------------------------------------------------------------
*/

if (
    !isset($_SESSION['user_id']) ||
    !isset($_SESSION['tenant_id'])
) {

    /*
    |--------------------------------------------------------------------------
    | LOCALHOST REDIRECT
    |--------------------------------------------------------------------------
    */

    if (
        $_SERVER['HTTP_HOST'] === 'localhost'
        || strpos($_SERVER['HTTP_HOST'], '127.0.0.1') !== false
    ) {

        header(
            "Location: /onyx_hotspot/index.php?error=expired"
        );

        exit;
    }

    /*
    |--------------------------------------------------------------------------
    | LIVE SERVER REDIRECT
    |--------------------------------------------------------------------------
    */

    header(
        "Location: /index.php?error=expired"
    );

    exit;
}

/*
|--------------------------------------------------------------------------
| TENANT SECURITY CHECK
|--------------------------------------------------------------------------
*/

if (
    (int)$_SESSION['tenant_id']
    !==
    (int)$safe_tenant_id
) {

    session_destroy();

    /*
    |--------------------------------------------------------------------------
    | LOCALHOST
    |--------------------------------------------------------------------------
    */

    if (
        $_SERVER['HTTP_HOST'] === 'localhost'
        || strpos($_SERVER['HTTP_HOST'], '127.0.0.1') !== false
    ) {

        header(
            "Location: /onyx_hotspot/index.php?error=tenant_mismatch"
        );

        exit;
    }

    /*
    |--------------------------------------------------------------------------
    | LIVE SERVER
    |--------------------------------------------------------------------------
    */

    header(
        "Location: /index.php?error=tenant_mismatch"
    );

    exit;
}

/*
|--------------------------------------------------------------------------
| LOAD CONFIG
|--------------------------------------------------------------------------
*/

require_once(__DIR__ . '/config.php');

/*
|--------------------------------------------------------------------------
| GLOBAL VARIABLES
|--------------------------------------------------------------------------
*/

$tenant_id =
(int)$_SESSION['tenant_id'];

$organization_name =
$_SESSION['organization']
?? 'Onyx Hub';

$nav_query =
"?tenant_id=" . $tenant_id;

?>